public class DateHrPair {
    private String date;
    private String isMeasured;
    public DateHrPair() { }
    public void setDate(String date) {
        this.date = date;
    }
    public void setIsMeasured(String isMeasured) {
        this.isMeasured = isMeasured;
    }
    public String getDate() {
        return date;
    }
    public String getIsMeasured() {
        return isMeasured;
    }
}
