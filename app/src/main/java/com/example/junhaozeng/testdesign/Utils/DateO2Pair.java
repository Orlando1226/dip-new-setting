public class DateO2Pair {
    private String date;
    private String isMeasured;
    public DateO2Pair() { }
    public void setDate(String date) {
        this.date = date;
    }
    public void setIsMeasured(String isMeasured) {
        this.isMeasured = isMeasured;
    }
    public String getDate() {
        return date;
    }
    public String getIsMeasured() {
        return isMeasured;
    }
}
